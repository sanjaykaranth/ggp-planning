	<p class="mainheading"><b>SHIPPING POLICY</b></p><br />


	<p>
PhiTesla currently ships its products to destinations within India using safe
and reliable courier services. Customers from outside India should contact our
sales department by email for assistance. In an attempt to offer our best
possible pricing to customers we may not keep all items in our
inventory. However, upon receipt of your order, our staff will establish
whether the ordered items are available for immediate shipment. If there is a
problem or delay in fulfilling your order, we will contact you by email.
</p>

<br />

<p class="subboldtan">In Stock Items</p>
<p>
Orders confirmed before 2 PM Indian Standard Time (IST) will be shipped the
same working day when possible. Shipments will be made Monday - Friday, except
Holidays.</p>

<br />

<p class="subboldtan">Non-Stock Items</p>

<p>
If the ordered items are not immediately available, an availability date will
be sent by e-mail. The items will be shipped immediately once they become
available.
</p>
