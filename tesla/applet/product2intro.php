<b>Radon: A Digital Display Unit with a Lot More</b> <br/>

<p>A completely versatile Multi-Information Display Unit equipped with an
On-Board Diagnostics (OBD) Computer to continuously monitor and display
parametric vehicle data. An array of sensors provide the system with essential
data like Speed, RPM, Temperature and Voltage that are processed and displayed
on a clearly visible backlit LCD panel. The RADON is a direct fit for the
Royal Enfield BULLET range of motorcycles and requires minimal modifications
if any for complete operation in other models of motorcycles too.
</p>

<b>FEATURES</b><br />
<ul>
<li>Speedometer large character display</li>
<li>Trip Meter and 7-Digit Digital Odometer </li>
<li>Programmable Shift RPM    </li>
<li>Digital RPM readout</li>
<li>Digital Engine Oil Temperature readout</li>
<li>Digital Battery Voltage readout</li>
<li>Digital Shift and Hazard Indicator</li>
<li>Super Bright LED Indicators</li>
</ul>

